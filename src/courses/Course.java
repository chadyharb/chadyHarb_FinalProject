    private void populate() {
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("Select *" + "From tbl_Courses Where crs_id =" + crsid);
            if(rs.next()) {
                txtcoursecode.setText(rs.getString("crs_code"));
                txtcoursename.setText(rs.getString("crs_name"));
                txtdescription.setText(rs.getString("crs_description"));
                cmbtype.setSelectedItem(rs.getString("crs_type"));
                cmbnbcredits.setSelectedItem(rs.getString("crs_nb_credits"));
                if(rs.getString("crs_lab").equals("Yes")){
                    cboxlab.setSelected(true);
                }else{
                    
                    cboxlab.setSelected(false);
                }
                
           
            } 
            
        } catch (SQLException ex) {
                    System.err.println(ex.getMessage());
                    }
        
    }
    
