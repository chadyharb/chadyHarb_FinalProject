    private void btsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsaveActionPerformed
        // TODO add your handling code here:
        if (txtcoursecode.getText().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "Enter a First Name", "Warning",
                    JOptionPane.WARNING_MESSAGE);
        } else if (txtcoursename.getText().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "Enter a Last Name", "Warning",
                    JOptionPane.WARNING_MESSAGE);
        } else {
            String coursecode = txtcoursecode.getText();
            String coursename = txtcoursename.getText();
            String gender;
            
            String lab;
            if (cboxlab.isSelected()) {
                lab = "Yes";
            } else {
                lab = "No";
            }
            String type= cmbtype.getSelectedItem().toString();
            int nbcredit=Integer.parseInt(cmbnbcredits.getSelectedItem().toString());
            
            String description = txtdescription.getText();
            
            try {
                PreparedStatement pstmt;
                if(crsid==0){
                       pstmt = con.prepareStatement("Insert Into "
                                + "tbl_courses (crs_code,"
                                + "crs_name, crs_description, "
                                + "crs_type, crs_nb_credits, "
                                + "crs_lab)"
                                + "Values ( '" + coursecode + "', "
                                + "'" + coursename + "', '" + description + "',' "
                                + type + "'," + nbcredit + ", "
                                + lab + "')");
                }else{
                    pstmt = con.prepareStatement("Update tbl_courses "
                            + "Set crs_code = '" + coursecode + "', "
                            + "crs_name = '" + coursename + "', "
                            + "crs_description = '" + description + "', "
                            + "crs_type = " + type + ", "
                            + "crs_nb_credits = '" + nbcredit + "', "
                            + "crs_lab = '" + lab +"'"
                            + "Where crs_id = " + crsid);
                }
                pstmt.execute();
                this.dispose();
            } catch (SQLException ex) {
                System.err.println(ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_btsaveActionPerformed

